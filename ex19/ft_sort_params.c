#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int		ft_strcmp(char *s1, char *s2)
{
	int i;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (0);
}

int		main(int argc, char *argv[])
{
	int flag;
	
	if (argc == 3)
	{ 
		flag = ft_strcmp(argv[1], argv[2]);
		if (flag > 0)
		{
			ft_putstr(argv[2]);
		}
		else 
			ft_putstr(argv[1]);
	}
	return (0);
}

