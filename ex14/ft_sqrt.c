int	ft_sqrt(int nb)
{
	int i;
	int sqr;

	i = 1;
	sqr = 1;
	while (sqr <= nb)
	{
		sqr = i * i;
		if (sqr == nb)
			return (i);
		i++;
	}
	return (0);
}
